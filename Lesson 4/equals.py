# We can assign things with equals.
# If you have a single equals you're assigning something.
name = "Liv"

statement = name + " loves Lucky Charms!"

print(statement)

# We can compare things with equals.
compare_statement1 = "Kipp loves Lucky Charms!"
compare_statement2 = "Liv loves Lucky Charms!"

print(statement == compare_statement1)
print(statement == compare_statement2)

# We have shortcuts with equals. 
# You have += and -=. 
# -= doesn't work with strings.
name = "Liv"

name += " loves Lucky Charms!"

print(name)
